package com.egitim.birincihafta.cmt.veritipleridegiskenler.primitivetypes;

public class PrimitiveNonNumeric {

    public static void main(String[] args) {
        /**
         * char : 16 bit
         */
        char a = 'a';
        System.out.println("char değeri:" + a + " , karakterin asci değeri: " +(int) a);

        /**
         * boolean : 1 bit. true, false değerini alır sadece.
         */
        boolean booleanValue = true;
        System.out.println(booleanValue);


    }

}
