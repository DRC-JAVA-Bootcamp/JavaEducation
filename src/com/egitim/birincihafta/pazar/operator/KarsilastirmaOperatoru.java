package com.egitim.birincihafta.pazar.operator;

public class KarsilastirmaOperatoru {
    public static void main(String[] args) {

        /**
         * Eşitlik : a == b
         * Eşit Değil : a != b
         * Büyüktür : a > b
         * Küçüktür : a < b
         * Büyük Eşittir : a >= b
         * Küçük Eşittir : a <= b
         */
        System.out.println(5 == 4);

        int a = 5;
        int b = 5;
        System.out.println(a == b);
    }
}
